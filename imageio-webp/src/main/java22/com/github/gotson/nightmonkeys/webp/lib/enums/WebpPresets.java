package com.github.gotson.nightmonkeys.webp.lib.enums;

/**
 * Enum representing different WebP presets.
 */
public enum WebpPresets {
    /**
     * Default preset.
     */
    DEFAULT,

    /**
     * Preset optimized for digital pictures, like portraits, inner shots.
     */
    PICTURE,

    /**
     * Preset optimized for outdoor photographs, with natural lighting.
     */
    PHOTO,

    /**
     * Preset optimized for hand or line drawings, with high-contrast details.
     */
    DRAWING,

    /**
     * Preset optimized for small-sized colorful images.
     */
    ICON,

    /**
     * Preset optimized for text-likes.
     */
    TEXT;
}