package com.github.gotson.nightmonkeys.jxl;

public class JxlException extends Exception {
    public JxlException(String message) {
        super(message);
    }

    public JxlException(String message, Throwable cause) {
        super(message, cause);
    }

    public JxlException(Throwable e) {
        super(e);
    }
}
